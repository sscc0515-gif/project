private void initializeTable() {
    DefaultTableModel model = new DefaultTableModel(new Object[]{"No", "날짜", "제목", "내용"}, 0);
    scheduleTable.setModel(model);
     // 테이블 열 크기 조정
    scheduleTable.getColumnModel().getColumn(0).setPreferredWidth(1);  // 번호 열 너비
    scheduleTable.getColumnModel().getColumn(1).setPreferredWidth(120); // 날짜 열 너비
    scheduleTable.getColumnModel().getColumn(2).setPreferredWidth(120); // 제목 열 너비
    scheduleTable.getColumnModel().getColumn(3).setPreferredWidth(300); // 내용 열 너비
    
        try {
        DBM.dbOpen();
       // strSQL = "SELECT * FROM EVENT ORDER BY event_no";
       //DBM.rs = DBM.stmt.executeQuery(strSQL);
        
        strSQL = "SELECT * FROM EVENT ORDER BY event_date"; //날짜순 정렬
        DBM.rs = DBM.stmt.executeQuery(strSQL);
        // DB에서 데이터를 읽어와 JTable에 추가
        while (DBM.rs.next()) {
            int no = DBM.rs.getInt("event_no");
            String date = DBM.rs.getString("event_date");
            String title = DBM.rs.getString("event_title");
            String content = DBM.rs.getString("event_content");
            model.addRow(new Object[]{no, date, title, content});
        }
        
        DBM.dbClose();
    } catch (Exception e) {
        System.out.println("SQLException: " + e.getMessage());
    }
        
}
