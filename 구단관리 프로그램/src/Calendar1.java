    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        
          int selectedRow = scheduleTable.getSelectedRow();
    
    // 일정이 선택되지 않으면 알림
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "수정할 일정을 선택하세요.");
        return;
    }
    
    // 선택된 일정의 event_no 가져오기
    DefaultTableModel model = (DefaultTableModel) scheduleTable.getModel();
    int eventNo = (int) model.getValueAt(selectedRow, 0); // 첫 번째 열은 event_no
    
    // 입력 필드에서 제목, 내용, 날짜 가져오기
    String title = titleField.getText();
    String description = descriptionField.getText();
    Date selectedDate = calendar.getDate();
    
    // 날짜를 yyyy-MM-dd 형식으로 변환
    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
    String formattedDate = dateFormat.format(selectedDate);
    
    // DB 업데이트 쿼리
    try {
        DBM.dbOpen(); // DB 연결
        
        // UPDATE 쿼리 준비
        String strSQL = "UPDATE EVENT SET event_date = ?, event_title = ?, event_content = ? WHERE event_no = ?";
        
        // PreparedStatement 객체 생성
        PreparedStatement pstmt = DBM.con.prepareStatement(strSQL);
        pstmt.setString(1, formattedDate); // 날짜 설정
        pstmt.setString(2, title);         // 제목 설정
        pstmt.setString(3, description);   // 내용 설정
        pstmt.setInt(4, eventNo);          // 수정할 event_no 설정
        
        // 쿼리 실행
        int updatedRows = pstmt.executeUpdate(); // DB에서 업데이트 수행
        
        // 업데이트 성공 여부 확인
        if (updatedRows > 0) {
            // 테이블의 해당 행 업데이트
            model.setValueAt(formattedDate, selectedRow, 1);
            model.setValueAt(title, selectedRow, 2);
            model.setValueAt(description, selectedRow, 3);
            
            JOptionPane.showMessageDialog(this, "일정이 수정되었습니다.");
        } else {
            JOptionPane.showMessageDialog(this, "수정에 실패했습니다.");
        }
        
        DBM.dbClose(); // DB 연결 종료
    } catch (SQLException e) {
        System.out.println("SQLException: " + e.getMessage());
        JOptionPane.showMessageDialog(this, "수정 중 오류가 발생했습니다.");
    }   catch (IOException ex) {
            Logger.getLogger(Calendar1.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    // 입력 필드 초기화
    titleField.setText("");
    descriptionField.setText("");
    }//GEN-LAST:event_updateButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        
            int selectedRow = scheduleTable.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "삭제할 일정을 선택하세요.");
        return;
    }
    
    // JTable에서 선택된 행의 event_no 값 가져오기
    DefaultTableModel model = (DefaultTableModel) scheduleTable.getModel();
    int eventNo = (int) model.getValueAt(selectedRow, 0); // 선택된 일정의 event_no

    // DB에서 해당 일정 삭제
    try {
        DBM.dbOpen(); // DB 연결 (conn이 올바르게 설정되어 있는지 확인)
        
        // DELETE 쿼리 준비
        String strSQL = "DELETE FROM EVENT WHERE event_no = ?";
        
        // PreparedStatement 객체 생성
        PreparedStatement pstmt = DBM.con.prepareStatement(strSQL);
        pstmt.setInt(1, eventNo); // 삭제할 일정의 event_no 값을 설정
        
        // 쿼리 실행
        int deletedRows = pstmt.executeUpdate(); // DB에서 삭제 수행
        if (deletedRows > 0) {
            // DB에서 삭제가 성공하면 JTable에서도 해당 행 삭제
            model.removeRow(selectedRow);
            JOptionPane.showMessageDialog(this, "일정이 삭제되었습니다.");
        } else {
            JOptionPane.showMessageDialog(this, "삭제에 실패했습니다.");
        }
        
        DBM.dbClose(); // DB 연결 종료
    } catch (Exception e) {
        System.out.println("SQLException: " + e.getMessage());
        JOptionPane.showMessageDialog(this, "삭제 중 오류가 발생했습니다.");
    }
    
    }//GEN-LAST:event_deleteButtonActionPerformed

