    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        String UNIFORM_NUM = (String) model.getValueAt(selectedRow, 0); // 선택된 부상정보 삭제

        // DB에서 해당 일정 삭제
        try {
            DBM.dbOpen(); // DB 연결 (conn이 올바르게 설정되어 있는지 확인)

            // DELETE 쿼리 준비
            String strSQL = "DELETE FROM health_stat WHERE UNIFORM_NUM = ?";

            // PreparedStatement 객체 생성
            DBM.pstmt = DBM.con.prepareStatement(strSQL);
            DBM.pstmt.setString(1, UNIFORM_NUM); // 삭제할 db 값을 설정

            // 쿼리 실행
            int deletedRows = DBM.pstmt.executeUpdate(); // DB에서 삭제 수행
            if (deletedRows > 0) {
                // DB에서 삭제가 성공하면 JTable에서도 해당 행 삭제
                model.removeRow(selectedRow);
                JOptionPane.showMessageDialog(this, "부상정보가 삭제되었습니다.");
            } else {
                JOptionPane.showMessageDialog(this, "삭제에 실패했습니다.");
            }

            DBM.dbClose(); // DB 연결 종료
        } catch (Exception e) {
            System.out.println("SQLException: " + e.getMessage());
            JOptionPane.showMessageDialog(this, "삭제 중 오류가 발생했습니다.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1AncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_jTable1AncestorAdded
        // TODO add your handling code here:
        DefaultTableModel tableModel = new DefaultTableModel();
        jTable1.setModel(tableModel);
        try{
            DBM.dbOpen();
            DBM.rs = DBM.stmt.executeQuery("Select * From health_stat");
            ResultSetMetaData metaData = DBM.rs.getMetaData();
            int columnCount = metaData.getColumnCount();
            // 테이블 모델의 컬럼 헤더 설정
            tableModel.setColumnCount(0);  // 기존 컬럼 지우기
            for (int i = 1; i <= columnCount; i++) {
                tableModel.addColumn(metaData.getColumnLabel(i));  // 컬럼 헤더 추가
            }

            // 데이터 행을 테이블 모델에 추가
            while (DBM.rs.next()) {
                Object[] rowData = new Object[columnCount];
                for (int i = 1; i <= columnCount; i++) {
                    rowData[i - 1] = DBM.rs.getObject(i);  // 각 컬럼 값을 가져와서 배열에 저장
                }
                tableModel.addRow(rowData);  // 행 추가
            }

            // DB 연결 종료
            DBM.dbClose();  // DB 연결 종료 메서드 호출 (예시)

        }catch(Exception e){
            System.out.print(e.getMessage());
        }
    }//GEN-LAST:event_jTable1AncestorAdded

