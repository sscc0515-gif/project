    public final String getPlayerInfo(String strQuery){
        String result = null;
        try{
            DBM.dbOpen();
            DBM.rs = DBM.stmt.executeQuery(strQuery);
            while(DBM.rs.next()){
                result += DBM.rs.getString("UNIFORM_NUM"+",");
                result += DBM.rs.getString("NAME"+",");
                result += DBM.rs.getString("HEIGHT"+",");
                result += DBM.rs.getString("WEIGHT"+",");
                result += DBM.rs.getString("BIRTH"+",");
                result += DBM.rs.getString("NATION"+",");
                result += DBM.rs.getString("POSITION"+",");
                result += DBM.rs.getString("CLUB" + "\n");
            }
            DBM.dbClose();
        }catch(Exception e){
            System.out.println("SQLException : "+e.getMessage());
        }
        return result;
    }
        
